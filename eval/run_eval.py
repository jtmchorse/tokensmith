class ReplaySource:
    """AnswerSource: returns the pre-generated CSS for an example id."""

    def __init__(self, responses: dict[str, str]) -> None:
        self._r = responses

    def answer(self, example: Example) -> str:
        return self._r[example.id]


class ReplayBackend:
    """Judge Backend: returns the recorded verdict for the response in the prompt.

    The harness's USER_TEMPLATE ends with 'RESPONSE: {response}\\n', so the
    response text is everything after the last 'RESPONSE: '. Verdicts are keyed
    by that exact text.
    """

    def complete(self, system: str, user: str) -> str:
        response = user.split("RESPONSE:", 1)[1].strip()
        score, reasoning = VERDICTS[response]
        return f"SCORE: {score}\nREASONING: {reasoning}\n"


def run_arm(suite: str, responses: dict[str, str], db_path: Path):
    spec = RunSpec(
        suite=suite,
        dataset_path=DATASET,
        judge=Judge(backend=ReplayBackend()),
        answer_source=ReplaySource(responses),
        judge_model="human-in-the-loop (replayed verdicts)",
        rubric=RUBRIC,
    )
    return run_suite(spec, db_path=db_path, run_id=f"{suite}-run")


def main() -> None:
    # sanity: every replayed verdict parses through the real harness parser
    for resp, (score, _reason) in VERDICTS.items():
        v = parse_judge_output(f"SCORE: {score}\nREASONING: x\n")
        assert abs(v.score - score) < 1e-9, resp

    RESULTS.mkdir(exist_ok=True)
    db = Path(tempfile.mkdtemp()) / "runs.db"

    control = run_arm("tokensmith-control", CONTROL, db)
    treatment = run_arm("tokensmith-treatment", TREATMENT, db)

    (RESULTS / "control_run.json").write_text(json.dumps(control.to_json(), indent=2, sort_keys=True))
    (RESULTS / "treatment_run.json").write_text(json.dumps(treatment.to_json(), indent=2, sort_keys=True))

    with connect(db) as conn:
        cur = read_run(conn, "tokensmith-treatment-run")
        base = read_run(conn, "tokensmith-control-run")
    # diff_runs guards against cross-suite diffs; align the suite label to view the delta.
    cur = replace(cur, suite=base.suite)
    delta = diff_runs(cur, base, threshold_drop=0.1)
    (RESULTS / "delta.json").write_text(json.dumps(delta.to_json(), indent=2, sort_keys=True))

    print("CONTROL   mean on-system score:", round(control.mean_score, 4))
    print("TREATMENT mean on-system score:", round(treatment.mean_score, 4))
    print("LIFT (treatment - control)   :", round(treatment.mean_score - control.mean_score, 4))
    print()
    print(render_delta_ascii(delta))


if __name__ == "__main__":
    main()
