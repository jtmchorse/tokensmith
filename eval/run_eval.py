"""Product-thesis eval: does an agent write on-system CSS WITH tokensmith vs WITHOUT?

Tokensmith's claim is "make coding agents design-system-aware." This eval turns
that claim into a number, using the llm-eval-harness
(https://github.com/jt-mchorse/llm-eval-harness).

Two arms over the same golden dataset (tokensmith_onsystem_v1.jsonl):
  - control   : CSS an agent writes from memory, no design system in context
  - treatment : CSS an agent writes given tokensmith's resolved token table

The two arms were generated by independent subagents (no leakage: the control
agent never saw the Meridian tokens). The verdicts below were authored by a
human-in-the-loop judge in the harness's SCORE/REASONING format and are replayed
through the real Judge -> parse_judge_output -> run_suite -> diff_runs pipeline;
only the model call is replaced, all harness machinery executes. Replaying fixed
verdicts also makes this script deterministic and API-key-free, so the recorded
numbers reproduce exactly.

Run:
    python -m venv .venv && . .venv/bin/activate
    pip install -r requirements.txt
    python run_eval.py

Scoring rubric (on-system-ness):
  1.0  literal equals the correct token's resolved value (on-system)
  ~0.1 near-miss: visually close but not the token value (silent off-system drift)
  0.0  fully off-system, not even close
Collision rows (ts_07, ts_08) also require the right SEMANTIC token name.
"""

